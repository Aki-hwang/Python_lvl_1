for i in range(100): #원소가 100가 들어가 있는  Range를 만들어 준다
    print("hello world")
    print(i)

for i in range(10, 51):  # 10부터 50까지 하려면 51해야해
    print("hello world")
    print(i)


for i in range(0, 101, 2):  # 0부터 100까지 2개씩 점프 즉 For문과 Range 함수간의 궁합이 좋아
    print("hello world")
    print(i)